package br.gov.eureka.model;

public class Tamanho {
	
	private int idTamanho;
	private String abreviacaoTamanho;
	private int idProduto;
	private  boolean pizzaProduto;
	private Double percentualProduto;
	public int getIdTamanho() {
		return idTamanho;
	}
	public void setIdTamanho(int idTamanho) {
		this.idTamanho = idTamanho;
	}
	public String getAbreviacaoTamanho() {
		return abreviacaoTamanho;
	}
	public void setAbreviacaoTamanho(String abreviacaoTamanho) {
		this.abreviacaoTamanho = abreviacaoTamanho;
	}
	public int getIdProduto() {
		return idProduto;
	}
	public void setIdProduto(int idProduto) {
		this.idProduto = idProduto;
	}
	public boolean isPizzaProduto() {
		return pizzaProduto;
	}
	public void setPizzaProduto(boolean pizzaProduto) {
		this.pizzaProduto = pizzaProduto;
	}
	public Double getPercentualProduto() {
		return percentualProduto;
	}
	public void setPercentualProduto(Double percentualProduto) {
		this.percentualProduto = percentualProduto;
	}
	
	
	
	
	
	
	
	
	
	
	
	
	

}
